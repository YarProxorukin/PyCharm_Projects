# N^2 + (N + 1)^2 + (N + 2)^2 + ... + (2N)2

n = int(input('Введите N: '))
s = n ** 2

for i in range(1, n + 1):
    s = s + (n + i) ** 2

s = s + (2 * n) ** 2
print('Сумма равна: ', s)
print('\nПрограмма завершена!')

